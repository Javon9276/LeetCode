package cn.javon.leetcode.easy._021_mergetwosortedlists;

/**
 * @author Javon
 * @since 2018-01-24
 **/
public class ListNode {

    int val;

    ListNode next;

    ListNode(int x) {
        val = x;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
